# builtin functions list

# print
print("This is a function!")

# type -- returns type of input
eedos = 0.2
print(type(eedos))

# input returns user response as string
response = input("hello")
print(response)
print(type(response))

# float -- returns input(numerical string or integer) converted to float type(decimal) 
var = float(23)
var2 = float("23")
print(var, var2)

# str -- returns input converted to string type(characters together)
name = str(35)
print(type(name))

# int -- returns input converted into integer(numbers)
erw =int(23.9)
erw2 = int("5")
print(erw, erw2)

# bool -- returns input coverted to boolean(True, False)
var3 = bool(0)
print(var3)

